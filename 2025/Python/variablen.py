# Variablen in Python

a = 3                                   #Festlegen der Variablen
b = 9

summe = a + b                           #Summe berechnen
print('Summe        :', summe)          #Summe ausgeben

differenz = a - b                       #Differenz berechnen
print('Differenz    :', differenz)      #Different ausgeben

produkt = a * b                         #Produkt berechnen
print('Produkt      :', produkt)        #Produkt ausgeben

qu1 = b/a                               #Quotient1 berechnen
print('b/a          :', qu1)            #Quotient1 ausgeben

qu2 = a/b                               #Quotient2 berechnen 
print('a/b          :', qu2)            #Quotient2 ausgeben

pot = b**a                              #b^a berechnen
print('b**a         :', pot)            #b^a ausgeben

p2 = b*qu2                              #Produkt 2 ist Gleitkommazahl
print('p2           :', p2)             #Produkt 2 ausgben
