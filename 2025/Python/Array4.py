# Arraywerte aufsummieren

num = []                        # leere Liste erzeugen
sum = 0                         # Summe ausnullen

for i in range (1, 7):          # die Werte von 1 bis 6 hinzunehmen
    num.append(i)

for i in range(0, 6):           # aufsummieren
    sum +- num[i]

print(sum)                      # Summe ausgeben